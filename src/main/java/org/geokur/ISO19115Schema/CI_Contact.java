    // occurrence and obligation
    private final String[] elementName = {"phone", "address", "onlineResource", "hoursOfService", "contactInstructions", "contactType"};
    private final int[] elementMax = {Integer.MAX_VALUE, Integer.MAX_VALUE, Integer.MAX_VALUE, Integer.MAX_VALUE, 1, 1};
    private final boolean[] elementObligation = {false, false, false, false, false, false};

    private final String className = this.getClass().getSimpleName();
    private final boolean[] elementUsed = new boolean[elementName.length];

