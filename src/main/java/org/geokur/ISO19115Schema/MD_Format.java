    // occurrence and obligation
    private final String[] elementName = {"formatSpecificationCitation", "amendmentNumber", "fileDecompressionTechnique", "medium", "formatDistributor"};
    private final int[] elementMax = {1, 1, 1, Integer.MAX_VALUE, Integer.MAX_VALUE};
    private final boolean[] elementObligation = {true, false, false, false, false};

    private final String className = this.getClass().getSimpleName();
    private final boolean[] elementUsed = new boolean[elementName.length];

    // class variables
